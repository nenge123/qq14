export function findZone(zone){
	if(locationIndex[zone]){
		return locationIndex[zone];
	}
	zone = zone.trim();
	for(const id in locationIndex){
		if(locationIndex[id].weatherRate){
			if(locationIndex[id].name === zone || locationIndexCnName[id] === zone){
				return locationIndex[id];
			}
		}
	}
	return null;
}
export function findWeather(zone){
	const Zone = findZone(zone);
	if(Zone){
		return Zone.weatherRate;
	}
	return null;
}